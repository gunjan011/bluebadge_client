import React, {Component} from 'react';
import './Poems.css';

class PoemFive extends Component {

    render() {
        return (
        <div className="poem">
            <h2>Blue Roses</h2><br></br>
            <h4>Rudyard Kipling</h4><br></br>
            <p>Roses red and roses white<br></br>
    Plucked I for my love's delight.<br></br>
    She would none of all my posies--<br></br>
    Bade me gather her blue roses.<br></br>
    <br></br>
    Half the world I wandered through,<br></br>
    Seeking where such flowers grew.<br></br>
    Half the world unto my quest<br></br>
    Answered me with laugh and jest.<br></br>
    <br></br>
    Home I came at wintertide,<br></br>
    But my silly love had died<br></br>
    Seeking with her latest breath<br></br>
    Roses from the arms of Death.<br></br>
    <br></br>
    It may be beyond the grave<br></br>
    She shall find what she would have.<br></br>
    Mine was but an idle quest--<br></br>
    Roses white and red are best!</p>
 </div>
        )
    }
}
export default PoemFive;
