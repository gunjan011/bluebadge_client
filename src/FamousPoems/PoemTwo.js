class PoemTwo extends Component {

    render() {
        return (
         <div className="poemtwo">
             <h2 id="dream">A Dream Within a Dream</h2><br></br>
             <h4>Edgar Allan Poe</h4><br></br>
             <p>Take this kiss upon the brow!<br></br>
          And, in parting from you now,<br></br>
          Thus much let me avow-<br></br>
          You are not wrong, who deem<br></br>
          That my days have been a dream;<br></br>
          Yet if hope has flown away<br></br>
          In a night, or in a day,<br></br>
          In a vision, or in none,<br></br>
          Is it therefore the less gone?<br></br>
          All that we see or seem<br></br>
          Is but a dream within a dream.<br></br>
          <br></br>
          I stand amid the roar<br></br>
          Of a surf-tormented shore,<br></br>
          And I hold within my hand<br></br>
          Grains of the golden sand-<br></br>
          How few! yet how they creep<br></br>
          Through my fingers to the deep,<br></br>
          While I weep- while I weep!<br></br>
          O God! can I not grasp<br></br>
          Them with a tighter clasp?<br></br>
          O God! can I not save<br></br>
          One from the pitiless wave?<br></br>
          Is all that we see or seem<br></br>
          But a dream within a dream?</p>
         </div>
        )
    }
}
export default PoemTwo;